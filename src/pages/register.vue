<script setup>
import axios from '@/utils/axios'
import { ref } from 'vue'
import { useRouter } from 'vue-router'
import { toast } from 'vue3-toastify'

const router = useRouter()

const form = ref({
  name: '',
  uname: '',
  email: '',
  password: '',
  password_confirmation: '',
})

const isPasswordVisible = ref(false)
const isPasswordConfirmationVisible = ref(false)
const loading = ref(false)

const register = async () => {
  loading.value = true

  try {
    const res = await axios.post('/auth/register-user', form.value)
    toast.success('Registration successful!')
    router.push('/')
  } catch (error) {
    if (error.response?.data?.errors) {
      const errors = error.response.data.errors

      Object.values(errors).forEach(err => {
        toast.error(err[0])
      })
    } else {
      toast.error('Something went wrong. Please try again.')
    }
  } finally {
    loading.value = false
  }
}
</script>


<template>
  <div class="auth-wrapper d-flex align-center justify-center pa-4">
    <div class="position-relative my-sm-16">
      <!--
        👉 Top shape
        <VImg
        :src="authV1TopShape"
        class="text-primary auth-v1-top-shape d-none d-sm-block"
        />

        👉 Bottom shape
        <VImg
        :src="authV1BottomShape"
        class="text-primary auth-v1-bottom-shape d-none d-sm-block"
        /> 
      -->

      <!-- 👉 Auth card -->
      <VCard class="auth-card" max-width="460" :class="$vuetify.display.smAndUp ? 'pa-6' : 'pa-0'">
        <VCardText>
          <h4 class="text-h4 mb-1">
            Adventure starts here 🚀
          </h4>
          <p class="mb-0">
            Make your learning easy and fun!
          </p>
        </VCardText>

        <VCardText>
          <VForm @submit.prevent="register">
            <VRow>
              <!-- Name -->
              <VCol cols="12">
                <VTextField v-model="form.name" autofocus label="Fullname" placeholder="Inbound" />
              </VCol>
              <!-- User Name -->
              <VCol cols="12">
                <VTextField v-model="form.uname" autofocus label="Username" placeholder="Ibn" />
              </VCol>
              <!-- Phone -->
              <VCol cols="12">
                <VTextField v-model="form.phone" autofocus label="Phobe" type="tel" placeholder="09025915463" />
              </VCol>
              <!-- email -->
              <VCol cols="12">
                <VTextField v-model="form.email" label="Email" type="email" placeholder="johndoe@email.com" />
              </VCol>

              <!-- password -->
              <VCol cols="12">
                <VTextField v-model="form.password" label="Password" autocomplete="password" placeholder="············"
                  :type="isPasswordVisible ? 'text' : 'password'"
                  :append-inner-icon="isPasswordVisible ? 'bx-hide' : 'bx-show'"
                  @click:append-inner="isPasswordVisible = !isPasswordVisible" />

                <!-- password confirmation -->
                <VTextField v-model="form.password_confirmation" label="Confirm Password" autocomplete="password"
                  class="mt-6" placeholder="············" :type="isPasswordConfirmationVisible ? 'text' : 'password'"
                  :append-inner-icon="isPasswordConfirmationVisible ? 'bx-hide' : 'bx-show'"
                  @click:append-inner="isPasswordConfirmationVisible = !isPasswordConfirmationVisible" />

                <VBtn class="my-6" block type="submit" :loading="loading" :disabled="loading">
                  Sign up
                </VBtn>
              </VCol>

              <!-- login instead -->
              <VCol cols="12" class="text-center text-base">
                <span>Already have an account?</span>
                <RouterLink class="text-primary ms-1" to="/login">
                  Sign in instead
                </RouterLink>
              </VCol>
            </VRow>
          </VForm>
        </VCardText>
      </VCard>
    </div>
  </div>
</template>

